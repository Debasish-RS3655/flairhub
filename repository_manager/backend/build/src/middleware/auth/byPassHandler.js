import { Keypair } from "@solana/web3.js";
// bypass the authentication for testing
// generates a temporary public key every authentication
export const ByPassAuth = (ctx) => (req, res, next) => {
    const keypair = Keypair.generate();
    const pubkey = keypair.publicKey.toBase58();
    console.log('Anonymous public key generated by Auth Bypass Handler: ', pubkey);
    res.locals.pubKey = pubkey;
    next();
};
